"""
Общий conftest.py для всех тестов.

1. Создаёт ОДИН QApplication на всю тестовую сессию. Qt разрешает только
   один экземпляр на процесс — если бы каждый тестовый файл создавал
   свой собственный QApplication на уровне модуля (как было раньше),
   второй и последующие модули либо падали бы, либо вели себя
   непредсказуемо при сборе тестов в одной сессии pytest.
2. autouse=True — фикстура подключается ко всем тестам автоматически,
   без необходимости явно её запрашивать в каждом тестовом файле.
"""
import sys

import pytest
from PyQt6.QtWidgets import QApplication


@pytest.fixture(scope="session", autouse=True)
def qapp():
    app = QApplication.instance()
    if app is None:
        app = QApplication(sys.argv)
    yield app
    # Не вызываем app.quit() — pytest сам завершит процесс. Принудительное
    # завершение Qt-приложения между тестами может привести к крашам при
    # повторном использовании виджетов в рамках одной сессии.
